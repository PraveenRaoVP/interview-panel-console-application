package com.interviewpanel.models.helpers;

public class PrintersAndFormatters {
    public static void showMessage(String message) {
        System.out.println(message);
    }

    public static void starLine() {
        System.out.println("********************************");
    }

    public static void dashLine() {
        System.out.println("--------------------------------");
    }

    public static void dottedLine() {
        System.out.println(".................................");
    }
}
